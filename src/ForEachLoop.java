
public class ForEachLoop {
	public static void main (String args[]) {
		String[] celebrities = {"Abc", "PQR", "XYZ"};
		for (String name:celebrities) {
		System.out.print(name);
		System.out.print(",");
		}	
		
		System.out.println("\n");
		int[]age= {20, 22, 24, 26};
		for (int x: age) {
			System.out.print(x);
			System.out.print(",");
	
		}
    }
}  

